// GET CHARACTER SPRITE
// ════════════════════════════════════════════════════════════════

export function getCharacterSprite(
  char: Character,
  palette: CharPalette
): SpriteData {
  let template: Cell[][]
  
  const frame = char.frame % 4
  
  if (char.state === 'type') {
    // Type animation
    switch (char.dir) {
      case 0: template = frame % 2 === 0 ? CHAR_DOWN_TYPE_1 : CHAR_DOWN_TYPE_2; break
      case 1: template = frame % 2 === 0 ? CHAR_LEFT_TYPE_1 : CHAR_LEFT_TYPE_2; break
      case 2: template = frame % 2 === 0 ? CHAR_RIGHT_TYPE_1 : CHAR_RIGHT_TYPE_2; break
      case 3: template = frame % 2 === 0 ? CHAR_UP_TYPE_1 : CHAR_UP_TYPE_2; break
      default: template = CHAR_DOWN_TYPE_1
    }
  } else if (char.state === 'read') {
    // Read animation
    switch (char.dir) {
      case 0: template = frame % 2 === 0 ? CHAR_DOWN_READ_1 : CHAR_DOWN_READ_2; break
      case 1: template = frame % 2 === 0 ? CHAR_LEFT_TYPE_1 : CHAR_LEFT_TYPE_2; break
      case 2: template = frame % 2 === 0 ? CHAR_RIGHT_TYPE_1 : CHAR_RIGHT_TYPE_2; break
      case 3: template = frame % 2 === 0 ? CHAR_UP_TYPE_1 : CHAR_UP_TYPE_2; break
      default: template = CHAR_DOWN_READ_1
    }
  } else if (char.state === 'walk') {
    // Walk animation
    switch (char.dir) {
      case 0: // Down
        template = frame === 0 ? CHAR_WALK_DOWN_1 
                : frame === 1 ? CHAR_WALK_DOWN_2 
                : frame === 2 ? CHAR_WALK_DOWN_3 
                : CHAR_WALK_DOWN_4
        break
      case 1: // Left
        template = frame === 0 ? CHAR_WALK_LEFT_1 
                : frame === 1 ? CHAR_WALK_LEFT_2 
                : frame === 2 ? CHAR_WALK_LEFT_3 
                : CHAR_WALK_LEFT_4
        break
      case 2: // Right
        template = frame === 0 ? CHAR_WALK_RIGHT_1 
                : frame === 1 ? CHAR_WALK_RIGHT_2 
                : frame === 2 ? CHAR_WALK_RIGHT_3 
                : CHAR_WALK_RIGHT_4
        break
      case 3: // Up
        template = frame === 0 ? CHAR_WALK_UP_1 
                : frame === 1 ? CHAR_WALK_UP_2 
                : frame === 2 ? CHAR_WALK_UP_3 
                : CHAR_WALK_UP_4
        break
      default:
        template = CHAR_WALK_DOWN_2
    }
  } else {
    // Idle - use standing frame
    switch (char.dir) {
      case 0: template = CHAR_WALK_DOWN_2; break
      case 1: template = CHAR_WALK_LEFT_2; break
      case 2: template = CHAR_WALK_RIGHT_2; break
      case 3: template = CHAR_WALK_UP_2; break
      default: template = CHAR_WALK_DOWN_2
    }
  }
  
  return resolveTemplate(template, palette)
}

